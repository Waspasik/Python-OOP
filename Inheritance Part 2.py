class Triangle:
    def __init__(self, a, b, c):
        self.a = a
        self.b = b
        self.c = c

    def perimeter(self):
        return sum((self.a, self.b, self.c))
    

class EquilateralTriangle(Triangle):
    def __init__(self, side):
        super().__init__(side, side, side)



